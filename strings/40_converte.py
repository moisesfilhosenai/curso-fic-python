"""
Exemplo de conversão de strings
"""

escola = "senai vila rezende"

print("Convertendo o texto %s para caixa alta %s " % (escola, escola.upper()))

print("Convertendo o texto %s para caixa baixa %s " % (escola, escola.lower()))

print("Convertendo o texto %s para letra maiúscula da primeira palavra %s " % (escola, escola.capitalize()))
