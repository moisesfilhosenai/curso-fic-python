"""
Exemplo de uso de módulo calculadora com import
"""

import calculadora

soma = calculadora.somar(10, 20)
print(f"O resultado da soma é {soma}")

subtracao = calculadora.subtrair(10, 20)
print(f"O resultado da subtração é {subtracao}")

multiplicacao = calculadora.multiplicar(10, 20)
print(f"O resultado da multiplicação é {multiplicacao}")

divisao = calculadora.dividir(10, 20)
print(f"O resultado da divisão é {divisao}")
