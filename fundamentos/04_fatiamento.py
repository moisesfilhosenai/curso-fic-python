
"""
Exemplo de fatiamento de strings em python
Preciso sempre colocar uma posição a mais
"""

cidade = "Piracicaba"

primeiras_4_letras = cidade[0:4]

print("As quatro primeiras letras da palavra %s são %s" % (cidade, primeiras_4_letras))

duas_ultimas_letras = cidade[-2:]

print("As duas últimas letras da palavras %s são %s" % (cidade, duas_ultimas_letras))
